getPokemonTypeColor( String type ) {

  switch ( type) {
    case 'bug':
        return pokeNixTypeBugColor;
      break;
    case 'dark':
        return pokeNixTypeDarkColor;
      break;
    case 'dragon':
        return pokeNixTypeDragonColor;
      break;
    case 'electric':
        return pokeNixTypeElectricColor;
      break;
    case 'fairy':
        return pokeNixTypeFairyColor;
      break;
    case 'fighting':
        return pokeNixTypeFightingColor;
      break;
    case 'fire':
        return pokeNixTypeFireColor;
      break;
    case 'flying':
        return pokeNixTypeFlyingColor;
      break;
    case 'ghost':
        return pokeNixTypeGhostColor;
      break;
    case 'grass':
        return pokeNixTypeGrassColor;
      break;
    case 'ground':
        return pokeNixTypeGroundColor;
      break;
    case 'normal':
        return pokeNixTypeNormalColor;
      break;
    case 'poison':
        return pokeNixTypePoisionColor;
      break;
    case 'psychic':
        return pokeNixTypePsychicColor;
      break;
    case 'rock':
        return pokeNixTypeRockColor;
      break;
    case 'steel':
        return pokeNixTypeSteelColor;
      break;
    case 'water':
        return pokeNixTypeWaterColor;
      break;
    default:
        return pokeNixTypeNormalColor;
      break;
  }
  
}

